#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int n;
	int z;

	i = 0;
	n = 0;
	z = 0;
	while ((str[i] >= 1 && str[i] <= 31) || str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			z *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = 10 * n + (str[i] - '0');
		i++;
	}
	return (z * n);
}
